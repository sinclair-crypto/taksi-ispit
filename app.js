function parseFromPDFText(text) {
  const lines = text.split("\n").map(l => l.trim()).filter(Boolean);
  const quizzes = [];
  let currentQuiz = null;
  let currentQuestion = null;

  for (let line of lines) {

    if (line.startsWith("## ")) continue;

    if (line.startsWith("### ")) {
      if (currentQuiz) quizzes.push(currentQuiz);

      const m = line.match(/(\d+)\.(\d+)/);
      const code = m ? `${m[1]}_${m[2]}` : "";

      currentQuiz = {
        title: line.replace("### ", ""),
        code,
        questions: []
      };
      continue;
    }

    if (/^\d+\./.test(line)) {
      currentQuestion = {
        number: parseInt(line),
        text: line.replace(/^\d+\.\s*/, ""),
        answers: {},
        correct: [],
        lessonCode: currentQuiz.code   // ‚úÖ ADD THIS
      };
      currentQuiz.questions.push(currentQuestion);
      continue;
    }

    const ans = line.match(/^([a-z])\)\s*(.+)/);
    if (ans && currentQuestion) {
      let txt = ans[2];
      const key = ans[1];

      if (txt.includes("**")) {
        txt = txt.replace(/\*\*/g, "");
        currentQuestion.correct.push(key);
      }

      currentQuestion.answers[key] = txt;
    }
  }

  if (currentQuiz) quizzes.push(currentQuiz);
  return quizzes;
}

function isQuestionCorrect(question, selected) {
  const correct = question.correct;

  // must select same amount
  if (selected.length !== correct.length) return false;

  // every correct must be selected
  return correct.every(c => selected.includes(c));
}

function generateExamQuestions(total) {
  const pools = data.map(d => [...d.questions]);
  const result = [];

  while (result.length < total) {
    for (let pool of pools) {
      if (!pool.length) continue;
      if (result.length >= total) break;

      const q = pool.splice(Math.floor(Math.random() * pool.length), 1)[0];
      result.push(q);
    }
  }

  return result.slice(0, total);
}

